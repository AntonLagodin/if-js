export function stringSearch(dataArr, str) {
  const resultSearch = [];
  for (let i = 0; i < dataArr.length; i++) {
    const searchItem = `${dataArr[i].country}, ${dataArr[i].city}, ${dataArr[i].hotel}`;
    if (searchItem.includes(str)) {
      resultSearch.push(searchItem);
    }
  }
  return resultSearch;
}
